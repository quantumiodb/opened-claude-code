#!/usr/bin/env bun
/**
 * Auto-generates stub files for modules missing from the source map extraction.
 *
 * Parses `tsc --noEmit` output to find TS2307 (cannot find module) errors,
 * resolves the file paths, and creates minimal stubs that export `any` for
 * all imported names.
 *
 * Usage: bun scripts/generate-stubs.ts
 */

import { execSync } from 'child_process';
import { existsSync, mkdirSync, writeFileSync, readFileSync } from 'fs';
import { dirname, resolve, join } from 'path';

const ROOT = resolve(import.meta.dir, '..');

interface MissingImport {
  fromFile: string;
  modulePath: string;
}

// Run tsc and capture TS2307 errors
console.log('Running tsc to find missing modules...');
let tscOutput: string;
try {
  tscOutput = execSync('npx tsc --noEmit 2>&1', {
    cwd: ROOT,
    encoding: 'utf-8',
    maxBuffer: 10 * 1024 * 1024,
  });
} catch (e: any) {
  tscOutput = e.stdout || '';
}

// Parse TS2307 errors
const errorRegex = /^(.+?)\(\d+,\d+\): error TS2307: Cannot find module '([^']+)'/gm;
const missing: MissingImport[] = [];
let match: RegExpExecArray | null;

while ((match = errorRegex.exec(tscOutput)) !== null) {
  missing.push({ fromFile: match[1], modulePath: match[2] });
}

console.log(`Found ${missing.length} TS2307 errors`);

// Resolve to absolute file paths
const resolvedPaths = new Set<string>();
const skippedModules = new Set<string>();

for (const { fromFile, modulePath } of missing) {
  // Skip npm packages (not relative/src paths)
  if (
    !modulePath.startsWith('.') &&
    !modulePath.startsWith('src/') &&
    !modulePath.startsWith('react/compiler-runtime')
  ) {
    skippedModules.add(modulePath);
    continue;
  }

  // Skip .md files (skill docs etc.)
  if (modulePath.endsWith('.md')) {
    continue;
  }

  // Skip react/compiler-runtime (handled by shim)
  if (modulePath === 'react/compiler-runtime') {
    continue;
  }

  // Resolve the import
  const fromDir = dirname(resolve(ROOT, fromFile));
  let resolved: string;

  if (modulePath.startsWith('src/')) {
    resolved = resolve(ROOT, modulePath);
  } else {
    resolved = resolve(fromDir, modulePath);
  }

  // Convert .js to .ts
  if (resolved.endsWith('.js')) {
    resolved = resolved.replace(/\.js$/, '.ts');
  }

  // Check if it's a tsx file
  if (!existsSync(resolved) && existsSync(resolved.replace(/\.ts$/, '.tsx'))) {
    resolved = resolved.replace(/\.ts$/, '.tsx');
  }

  // Skip if file already exists
  if (existsSync(resolved)) {
    continue;
  }

  resolvedPaths.add(resolved);
}

console.log(`\nSkipped npm modules: ${[...skippedModules].join(', ')}`);
console.log(`\nGenerating ${resolvedPaths.size} stub files...\n`);

// Now find what names are imported from each missing module
const importRegex = /import\s+(?:type\s+)?{([^}]+)}\s+from\s+['"]([^'"]+)['"]/g;
const defaultImportRegex = /import\s+(\w+)\s+from\s+['"]([^'"]+)['"]/g;

